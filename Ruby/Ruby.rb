module LogFormatter
	def self.format_log
		#Net::HTTP.getを使ってネットからデータを取得したい
		#URLの文字列では渡すことができないので、URI.parseを使って、URI::HTTPSオブジェクトに変換
		uri = URI.parse("https://samples.jnito.com/access-log.json")
		#uri #=> #<URI::HTTPS https://samples.jnito.com/access-log.json>
		json = Net::HTTP.get(uri)
		
		#jsonデータをRubyのオブジェクトに変換(ハッシュ要素を持つ配列)
		log_data = JSON.parse(json, symbolize_names: true)
		#整形して出力
		pp log_data
	end
end


