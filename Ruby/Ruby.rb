****[クラスの作成とインスタンスの作成]====================
***{Classの概要/関連用語の解説}-------------
**クラスはデータ型の一種。オブジェクトの設計図(雛形)とも呼ばれる。Rubyにおいてはすべてのオブジェクトは何らかのクラスに属している。クラスは内部にデータを保持でき、自分が保持しているデータを利用する独自のメソッドを持つことができる。データとメソッドがセットになることで大きなプログラムを書く時に整理しやすい。
*クラスを定義すると、存在しない属性の追加や、誤った属性名の指定、値の書き換えを使用とするとerrorが起きる.

**オブジェクト(/インスタンス/レシーバ) --OOPにおいては、クラスからオブジェクトが生成され、クラスと同じ属性(データ項目)やメソッドを持ち、その属性値はオブジェクトにより異なる。
+インスタンス --殊クラスから生成されたことを強調する時にインスタンスと呼ばれる
+レシーバ --メソッドが呼び出された時に、そのメソッドの対象(呼び出された先)となるオブジェクトを強調する呼称。

*メソッド(/メッセージ) --オブジェクトの持つ'動作'/'振る舞い'のこと。処理のまとまりを名づけ、再利用できるようにしたもの。(関数/サブルーチンとも呼ばれる)

*状態(ステート) --各オブジェクトに保持されるデータをオブジェクトの状態(state)と呼ぶ

**属性(attribute,property) --オブジェクトの状態(state)を外部から取得/変更(get/set)できる場合があり、その値を属性(attribute)と呼ぶ。

**オープンクラス --Rubyのクラスというものは、変更に対してopenであるため、オープンクラスなどと呼ばれる。(組み込みクラスにメソッドを追加したり、上書きできる)

**モンキーパッチ --(クラスに限った話ではないが)既存のメソッドを上書きするなどによって、既存の実装を書き換え、自分の期待する挙動に変更すること。

