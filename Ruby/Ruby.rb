***[Hashクラス]=========================
ハッシュ値を用いてデータを管理するハッシュテーブル構造をとるため、ちなんでHashと名づけられた
データをキーと値で管理するオブジェクト(他言語の連想配列に相当する)。ハッシュのキーにはよくシンボルが用いられる。
*ハッシュに対して存在しないキーを渡すとnilが返ってくる
キーや値のデータ型(文字列、数値、シンボル、ハッシュ、配列)は混同していても良い。(キーのデータ型混同は非推奨)。
*変数 = {キー1 => 値1, キー2 => 値2} --ハッシュリテラル(構文)。
